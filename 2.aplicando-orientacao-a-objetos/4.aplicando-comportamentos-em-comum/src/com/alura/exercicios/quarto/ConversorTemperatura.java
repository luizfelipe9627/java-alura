package com.alura.exercicios.quarto;

public interface ConversorTemperatura {
  double celsiusParaFahrenheit(double temperaturaCelsius); // Método que converte a temperatura de celsius para fahrenheit, recebendo como parâmetro um double da temperatura em celsius e retornando um valor do tipo double.
  double fahrenheitParaCelsius(double temperaturaFahrenheit); // Método que converte a temperatura de fahrenheit para celsius, recebendo como parâmetro um double da temperatura em fahrenheit e retornando um valor do tipo double.
}
