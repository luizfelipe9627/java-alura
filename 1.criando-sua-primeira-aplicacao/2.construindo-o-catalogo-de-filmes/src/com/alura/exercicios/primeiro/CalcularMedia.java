package com.alura.exercicios.primeiro;

public class CalcularMedia {
  public static void main(String[] args) {
    //* Crie um programa que realize a média de duas notas decimais e exiba o resultado.

    int nota1 = 9; // Criado uma variável inteira chamada nota1 e atribuído o valor 9.
    double nota2 = 8.2; // Criado uma variável double chamada nota2 e atribuído o valor 8.2.
    double media = (nota1 + nota2) / 2; // Calcula a média das notas e armazena na variável media.
    System.out.println("A média é: " + media); // Exibe a média no console.
  }
}
