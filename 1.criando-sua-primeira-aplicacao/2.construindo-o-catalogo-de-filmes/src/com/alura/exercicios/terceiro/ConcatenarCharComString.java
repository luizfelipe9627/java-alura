package com.alura.exercicios.terceiro;

public class ConcatenarCharComString {

  public static void main(String[] args) {
    //* Declare uma variável do tipo char (letra) e uma variável do tipo String(palavra). Atribua valores a essas variáveis e concatene-as em uma mensagem.

    char letra = 'A'; // Criado uma variável char chamada letra e atribuído o valor 'A'.
    String texto = "A primeira letra do alfabeto é: "; // Criado uma variável String chamada texto e atribuído o valor "A primeira letra do alfabeto é: ".
    System.out.println(texto + letra); // Exibe a mensagem concatenada.
  }
}
